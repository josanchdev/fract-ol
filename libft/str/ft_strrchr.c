#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = ft_strlen(s);
	s += i;
	while (i-- && (char)c != *s)
	{
		s--;
	}
	if (*s == (char)c)
		return ((char *)s);
	return (0);
}
